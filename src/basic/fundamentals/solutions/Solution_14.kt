package basic.fundamentals.solutions

fun main(){
    val xByte: Byte = 2
    val xInt: Int = 100
    val xLong: Long = 27665553L
    val xFloat: Float = 3.45F
    val xDouble: Double = 52662.993877736
    val xBoolean: Boolean = true
